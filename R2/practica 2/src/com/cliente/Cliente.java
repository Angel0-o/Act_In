    private void ButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBorrarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblProductos.getModel();
        if (tblProductos.getSelectedRow() == -1) {
            if (tblProductos.getRowCount() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Necesitas seleccionar lo que desea eliminar");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Necesitas seleccionar lo que desea eliminar");
            }
        } else {
            model.removeRow(tblProductos.getSelectedRow());
        }
    }//GEN-LAST:event_ButtonBorrarActionPerformed

    private void ButtonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonComprarActionPerformed
        // TODO add your handling code here:
        int numeroCompras = 0;
        DefaultTableModel model = (DefaultTableModel) tblProductos.getModel();
        numeroCompras = model.getRowCount();
        ArrayList<Integer> compras = new ArrayList<Integer>(numeroCompras);
        if (tblProductos.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "No has comprado nada :(");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Tienes " + model.getRowCount() + " articulos en tu carrito.");
            for (int i = 0; i < numeroCompras; i++) {
                System.out.println("Id articulo comprado: " + (int) model.getValueAt(i, 2));
                compras.add((int) model.getValueAt(i, 2));
            }

            //Seteamos el tama√±o del arraylist de las compras
            this.reporte = new ArrayList<Producto>(compras.size());
            try {
                oos = new ObjectOutputStream(cl.getOutputStream());
                oos.writeObject(compras);
                oos.flush();
            } catch (IOException e) {
                e.printStackTrace();
            }

            try {
                ois = new ObjectInputStream(cl.getInputStream());
                
                reporte = (ArrayList<Producto>) ois.readObject();
            
            } catch (IOException e) {
                e.printStackTrace();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
            }

            ButtonReporte.setEnabled(true);
        }
    }//GEN-LAST:event_ButtonComprarActionPerformed

