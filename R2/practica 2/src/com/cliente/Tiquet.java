    private void ButtonGeneraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGeneraActionPerformed
        Date date = new Date();
        String result = "";
        double totalPagar = 0.0;
        
        for (int i = 0; i < producto.size(); i++) {

            result+=producto.get(i).toString()+"\n";
        }
        for (int i = 0; i < producto.size(); i++) {
            totalPagar+=producto.get(i).getPrecio();
        }
       
        TextArea.setText(date.toString()+"\n"+result + "Total a Pagar: $"+String.valueOf(totalPagar));
        //Crear el ticket de compra
        Document documento = new Document();
        FileOutputStream ticketPdf;
        try {
            ticketPdf = new FileOutputStream("ticketDeCompra.pdf");
            try {
                PdfWriter.getInstance(documento,ticketPdf).setInitialLeading(20);
                documento.open();
                
                documento.add(new Paragraph("Tienda deportiva Cristopher y Marco SA de CV",
				FontFactory.getFont("arial",   // fuente
				22,                            // tamaÃ±o
				Font.ITALIC,                   // estilo
				BaseColor.BLUE)));             // color
                
                try{
                    Image foto = Image.getInstance("logoCM.png");
                    foto.scaleToFit(100, 100);
                    foto.setAlignment(Chunk.ALIGN_MIDDLE);
                    documento.add(foto);
                   }
                catch ( Exception e ){
                    e.printStackTrace();}
                
                documento.add(new Paragraph("Fecha y hora de la compra: " + date.toString() +"\n"));
                
                PdfPTable tabla = new PdfPTable(3);
                    
                tabla.addCell("ID");
                tabla.addCell("Producto");
                tabla.addCell("Precio");
                for (int i = 0; i < producto.size(); i++) {
                    tabla.addCell(producto.get(i).getIdString());
                    tabla.addCell(producto.get(i).getNombre());
                    tabla.addCell(producto.get(i).getPrecioString());
                }
                tabla.addCell("");
                tabla.addCell("TOTAL");
                tabla.addCell(String.valueOf(totalPagar));
                documento.add(tabla);

                documento.close();
            } catch (DocumentException ex) {
                Logger.getLogger(Tiquet.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Tiquet.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_ButtonGeneraActionPerformed

    
